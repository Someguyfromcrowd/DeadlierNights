package someguy.deadliernights;

/**
 * An enum to specify what kind of scare a particular Scare object is
 * 
 * @author Someguyfromcrowd
 * 
 */
public enum ScareType
{
	/**
	 * Plays a fake sound to the player
	 */
	FAKESOUND, /**
	 * Briefly displays a fake mob to the player
	 */
	FAKEMOB, /**
	 * Fakes an injury
	 */
	FAKEHURT
}
